ll power(int a,int b,int mod){
    if(b==0) return 1;
    if(b==1) return a;
    if(b%2==0){
        return power((a*1LL*a)%mod,b/2,mod);
    }
    else{
        return (a*1LL*power(a,b-1,mod))%mod;
    }
}

